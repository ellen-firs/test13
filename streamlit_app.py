import streamlit as st
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# Заголовок интерфейса
st.title("AgroHunters: Платформа для поиска сотрудников")

# Боковое меню
menu = ["Главная", "Поиск сотрудников", "Мой профиль", "Статистика", "Обратная связь"]
choice = st.sidebar.selectbox("Выберите раздел", menu)

# Главная страница
if choice == "Главная":
    st.subheader("Добро пожаловать на платформу AgroHunters!")
    st.write("""
        Мы предоставляем возможность найти квалифицированных сотрудников для работы в аграрном секторе.
        Вы можете найти специалистов для различных задач: от сельского хозяйства до проектного управления.
    """)

# Страница поиска сотрудников
elif choice == "Поиск сотрудников":
    st.subheader("Поиск сотрудников")
    position = st.selectbox("Выберите роль", ["Агроном", "Механик", "Проектировщик"])
    location = st.text_input("Город")
    availability = st.radio("Доступность", ["Свободен", "Занят"])

    # Демонстрация результатов
    if st.button("Поиск"):
        st.write(f"Ищем {position} в городе {location} с доступностью: {availability}")
        data = pd.DataFrame({
            'Имя': ['Иван Иванов', 'Ольга Смирнова', 'Петр Павлов'],
            'Роль': ['Агроном', 'Механик', 'Проектировщик'],
            'Город': ['Москва', 'Санкт-Петербург', 'Новосибирск'],
            'Опыт': ['3 года', '5 лет', '2 года']
        })
        st.dataframe(data)

# Мой профиль
elif choice == "Мой профиль":
    st.subheader("Ваш профиль")
    name = st.text_input("Имя", value="Иван Иванов")
    email = st.text_input("E-mail", value="ivan@example.com")
    bio = st.text_area("О себе", "Я опытный агроном с 3-летним стажем работы в агропромышленном комплексе.")

    st.button("Обновить профиль")

# Статистика
elif choice == "Статистика":
    st.subheader("Статистика использования платформы")
    
    # Генерация данных для графика
    data = np.random.randn(1000)
    fig, ax = plt.subplots()
    ax.hist(data, bins=30)
    st.pyplot(fig)

    st.write("""
        График отображает распределение данных по всем пользователям платформы
        и показывает, как часто они используют различные функции, такие как поиск сотрудников и создание заявок.
    """)

# Обратная связь
elif choice == "Обратная связь":
    st.subheader("Оставьте ваш отзыв")
    
    # Форма обратной связи
    feedback = st.text_area("Ваш отзыв", "Платформа удобна в использовании, но есть некоторые улучшения.")
    rating = st.slider("Оцените платформу", 1, 5, 3)
    
    if st.button("Отправить"):
        st.success(f"Спасибо за ваш отзыв! Оценка: {rating}/5")

    st.write("Ваше мнение важно для нас, мы продолжаем улучшать платформу!")
